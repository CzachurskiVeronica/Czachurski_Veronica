resultado = A + B;  /*Realiza una operación aritmética, sumando el contenido de ambas variables
                      y las reserva en una nueva variable (resultado) */    
alert (`La suma de A+B es: ${resultado}`); //Muestra el resultado de la operación de sumar ambos valores
alert (`La diferencia de A-B es: ${A-B}`); //Muestra el resultado de la operación de restar ambos valores
alert (`La multiplicación de AxB es: ${A*B}`); //Muestra el resultado de la operación de multiplicar ambos valores
